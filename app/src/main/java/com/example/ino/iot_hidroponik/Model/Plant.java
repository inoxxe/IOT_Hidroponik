package com.example.ino.iot_hidroponik.Model;

public class Plant {

    private String name,id;

    public Plant() {
    }

    public Plant(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
